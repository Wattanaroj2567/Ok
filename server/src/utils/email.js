if (process.env.NODE_ENV === 'development') {
    debug('Email service is in DEVELOPMENT mode. Using Ethereal for testing.');
    nodemailer.createTestAccount((err, account) => {
        if (err) {
            console.error(
                '‚ùå Failed to create an Ethereal test account. ' + err.message,
            );
            return process.exit(1);
        }
        debug('‚úÖ Ethereal test account created successfully.');
        transporter = nodemailer.createTransport({
            host: account.smtp.host,
            port: account.smtp.port,
            secure: account.smtp.secure,
            auth: { user: account.user, pass: account.pass },
        });
    });
} else {
    debug('Email service is in PRODUCTION mode.');
    transporter = nodemailer.createTransport({
        host: process.env.EMAIL_HOST,
        port: process.env.EMAIL_PORT,
        secure: process.env.EMAIL_PORT == 465,
        auth: {
            user: process.env.EMAIL_USER,
            pass: process.env.EMAIL_PASS,
        },
    });
}

